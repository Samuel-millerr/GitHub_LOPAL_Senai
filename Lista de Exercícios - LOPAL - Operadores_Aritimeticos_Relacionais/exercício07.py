#EXERCÍCIO 07 - MÉDIA DE 3 NÚMEROS
#PEDE OS NÚMEROS AO USUÁRIO
numero01 = float(input("Insira o primeiro número: "))
numero02 = float(input("Insira o segundo número: "))
numero03 = float(input("Insira o terceiro número: "))

#REALIZAR A CONTA
media = (numero01 + numero02 + numero03)/3

#IMPRIMIR O RESULTADO
print(f"A média dos valores é {media:.1f}")

